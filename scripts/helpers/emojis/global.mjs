export function swapOrNot(text, emoji){
    if(!getThisModuleSetting('prettierEmojis') || !emoji){
        return text;
    }
    return emoji;
}

export function getDieEmoji(faces, result = "1"){
    if(!getThisModuleSetting('prettierEmojis') || !dice[faces]){
        return undefined;
    }
    return dice[faces][result];
}

export function getDocumentEmoji(doctype){
    if(!getThisModuleSetting('prettierEmojis') || !documents[doctype]){
        return doctype;
    }
    return documents[doctype];
}

export function dieIcon(faces = 6){
    if(!getThisModuleSetting('prettierEmojis') || !dice[faces].icon){
        return ":game_die:";
    }
    else return dice[faces].icon;
}